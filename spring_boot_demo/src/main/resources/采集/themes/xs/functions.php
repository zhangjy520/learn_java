function colorCloud($text) {
	$text=preg_replace_callback('|<a (.+?)>|i','colorCloudCallback',$text);
	return$text;
	}
function colorCloudCallback($matches) {
	$text=$matches[1];
	$color=dechex(rand(0,16777215));
	$pattern='/style=(\'|\”)(.*)(\'|\”)/i';
	$text=preg_replace($pattern,"style=\"color:#{$color};$2;\"",$text);
	return"<a $text>";
	}
