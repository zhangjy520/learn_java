package cn.gukeer.platform.modelView;

public class HotTopicView {
   private String id;
   private String name;
   private String weibo_num;
   private String url;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getWeibo_num() {
		return weibo_num;
	}

	public void setWeibo_num(String weibo_num) {
		this.weibo_num = weibo_num;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}
}
